    private static void totalPriceCalculator() {
        int quantity = Integer.parseInt(jtf_stockQuantity.getSelectedItem().toString());
        double totalPrice = stock.getBuyPrice() * quantity;
        jtf_stockPrice1.setText(moneyWriter(totalPrice));
        updatePortfolioDetails(totalPrice);
    }
    
    private static void updatePortfolioDetails(double totalPrice) {
        double portMoney = portfolio.getLiquidMoney() - totalPrice;
        double investedMoney = portfolio.getInvestedMoney() + totalPrice;
        
        if (portMoney < 0) {
            moneyError = true;
            jtf_balance.setBackground(Color.red);
        } else {
            moneyError = false;
            jtf_balance.setBackground(balColor);
            jtf_balance.setText(moneyWriter(portMoney));
            jtfInvestedMoney.setText(moneyWriter(investedMoney));
        }
    }

