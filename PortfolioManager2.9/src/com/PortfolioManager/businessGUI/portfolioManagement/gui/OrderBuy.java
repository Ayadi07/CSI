    public static double moneyReader(String moneyString) {
        StringTokenizer st = new StringTokenizer(moneyString, ",");
        if (moneyString.contains("$")) {
            return 0.0;
        } else {
            double money = Double.parseDouble(st.nextToken()) * 1000;
            money += Double.parseDouble(st.nextToken());
            return money;
        }
    }
    
    public static double roundDBL(double dollars) {
        int decimalPlace = 6;
        BigDecimal bd = new BigDecimal(dollars);
        bd = bd.setScale(decimalPlace, BigDecimal.ROUND_UP);
        
        return (bd.doubleValue());
    }
    
    public static String moneyWriter(double moneyValue) {
        String money = "";
        int millions = (int) moneyValue / 1000;
        money += millions + ",";
        double dollars = moneyValue - millions * 1000;
        dollars = roundDBL(dollars);
        if (dollars < 10) {
            money += "00" + dollars;
        } else if (dollars < 100) {
            money += "0" + dollars;
        } else {
            money += "" + dollars;
        }
        return money;
    }
    
