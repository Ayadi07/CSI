    private void cmb_portfItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmb_portfItemStateChanged
        if (evt.getStateChange() == java.awt.event.ItemEvent.SELECTED) {
            //table update
            IPortfolioDAO portfolioDAO = new PortfolioDAO();
            Portfolio portfolio = portfolioDAO.getPortfolioByName(cmb_portf.getSelectedItem().toString(), GUI.getMe().getLoggedInvestor().getAccount().getId());
            TableModel tableModel = jTable.getModel();
            tableModel.setValueAt(portfolio.getInvestedMoney(), 0, 0);
            tableModel.setValueAt(portfolio.getPotentialEarnings(), 0, 1);
            tableModel.setValueAt(portfolio.getPortfolioPerformance(), 0, 2);
            jTable.setModel(tableModel);
        }
    }//GEN-LAST:event_cmb_portfItemStateChanged

    private void btnSelectMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSelectMouseClicked
        IPortfolioDAO portfolioDAO = new PortfolioDAO();
        Portfolio portfolio = portfolioDAO.getPortfolioByName(cmb_portf.getSelectedItem().toString(), account.getId());
        account.setSelectedPortfolio(portfolio.getId());
        IAccountDAO iAccountDAO = new AccountDAO();
        iAccountDAO.update(account);
        JOptionPane.showMessageDialog(this, "Portfolio: " + portfolio.getName() + " is selected!", "Portfolio selected", JOptionPane.INFORMATION_MESSAGE);
        GUI.getMe().getLoggedInvestor().getAccount().setSelectedPortfolio(portfolio.getId());
        GUI.getMe().getCmb_market().setSelectedItem(portfolio.getMarket().getMarketId());
        GUI.getMe().UpdateJTable(true, true, true,true);
        this.setAlwaysOnTop(false);
        this.setVisible(false);
    }//GEN-LAST:event_btnSelectMouseClicked

