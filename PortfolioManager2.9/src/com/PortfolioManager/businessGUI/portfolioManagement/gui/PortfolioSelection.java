    public static void initPortfoliosList() {
        IPortfolioDAO portfolioDAO = new PortfolioDAO();
        cmb_portf.removeAllItems();
        listPortfolios = portfolioDAO.getPortfoliByAccount(GUI.getMe().getLoggedInvestor().getAccount().getId());
        for (Portfolio p : listPortfolios) {
            cmb_portf.addItem(p.getName());
        }
    }

